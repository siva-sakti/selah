/// Selah spacing scale
/// Consistent 4-point grid system
class SelahSpacing {
  SelahSpacing._();

  /// 4px - Minimal spacing
  static const double xs = 4.0;

  /// 8px - Tight spacing
  static const double sm = 8.0;

  /// 12px - Compact spacing
  static const double md = 12.0;

  /// 16px - Standard spacing
  static const double lg = 16.0;

  /// 24px - Comfortable spacing
  static const double xl = 24.0;

  /// 32px - Generous spacing
  static const double xxl = 32.0;

  /// 48px - Section spacing
  static const double xxxl = 48.0;

  /// 64px - Large section spacing
  static const double huge = 64.0;

  /// Screen edge padding
  static const double screenPadding = 24.0;

  /// Card internal padding
  static const double cardPadding = 16.0;

  /// Button internal padding horizontal
  static const double buttonPaddingH = 32.0;

  /// Button internal padding vertical
  static const double buttonPaddingV = 16.0;
}
